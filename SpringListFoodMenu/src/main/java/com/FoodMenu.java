package com;

import java.util.List;

public class FoodMenu {
	List items;
	List type;
	List cost;

	public List getItems() {
		return items;
	}

	public void setItems(List items) {
		this.items = items;
	}

	public List getType() {
		return type;
	}

	public void setType(List type) {
		this.type = type;
	}

	public List getCost() {
		return cost;
	}

	public void setCost(List cost) {
		this.cost = cost;
	}

}
